package jdr;

public class CharacterAttIllegalValue extends CharacterException {

	public CharacterAttIllegalValue() {
	}

	public CharacterAttIllegalValue(String message) {
		super(message);
	}

	public CharacterAttIllegalValue(Throwable cause) {
		super(cause);
	}

	public CharacterAttIllegalValue(String message, Throwable cause) {
		super(message, cause);
	}

	public CharacterAttIllegalValue(String message, Throwable cause, boolean enableSuppression,
			boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

}
